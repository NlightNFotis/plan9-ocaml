type t = {
  r: int8;
  g: int8;
  b: int8;
  a: int8;
}
and int8 = int

type rgba = t



(* less: sanity check between 0 and 255 *)
let mk r g b a =
  { r = r; g = g; b = b; a = a }

let opaque = mk 0xFF 0xFF 0xFF 0xFF
let transparent = mk 0x00 0x00 0x00 0x00

let black = mk 0x00 0x00 0x00 0xFF
let white = mk 0xFF 0xFF 0xFF 0xFF

let red   = mk 0xFF 0x00 0x00 0xFF
let green = mk 0x00 0xFF 0x00 0xFF
let blue  = mk 0x00 0x00 0xFF 0xFF

(* less: cyan, magenta, ... *)

let not_a_color = mk 0xFF 0xFF 0xFF 0x00
let nofill = not_a_color

